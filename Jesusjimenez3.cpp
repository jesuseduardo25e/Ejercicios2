#include <stdio.h>
#include <math.h>

int main() {
    float x, y, z;
    float cuadrado_x, cuadrado_y, cubo_z;
    float resultado;

    // Solicitar al usuario que ingrese los valores de x, y y z
    printf("Ingrese el valor de x: ");
    scanf("%f", &x);
    printf("Ingrese el valor de y: ");
    scanf("%f", &y);
    printf("Ingrese el valor de z: ");
    scanf("%f", &z);

    // Calcular el cuadrado de x, el cuadrado de y y el cubo de z
    cuadrado_x = pow(x, 2);
    cuadrado_y = pow(y, 2);
    cubo_z = pow(z, 3);

    // Sumar los resultados
    resultado = cuadrado_x + cuadrado_y + cubo_z;

    // Mostrar el resultado
    printf("El resultado es %.2f\n", resultado);

    return 0;
}
